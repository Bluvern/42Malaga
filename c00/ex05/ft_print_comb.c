#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putchar_multi(char ca, char cb, char cc)
{
	ft_putchar (ca + 48);
	ft_putchar (cb + 48);
	ft_putchar (cc + 48);
	if (ca != 7 || cb != 8 || cc != 9)
		write(1, ", ", 2);
}

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	while (a <= 7)
	{
		b = a + 1;
		while (b <= 8)
		{
			c = b + 1;
			while (c <= 9)
			{
				ft_putchar_multi (a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
