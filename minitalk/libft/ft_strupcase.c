#include "libft.h"

char	*ft_strupcase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		while (str[n] >= 'a' && str[n] <= 'z')
		{
			str[n] -= 32;
			n++;
		}
		n++;
	}
	return (str);
}
