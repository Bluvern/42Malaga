#include "libft.h"

char	*ft_strlowcase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		while (str[n] >= 'A' && str[n] <= 'Z')
		{
			str[n] += 32;
			n++;
		}
		n++;
	}
	return (str);
}
