#include "libft.h"

int	ft_str_is_numeric(char *str)
{
	int	ch;
	int	n;

	ch = 1;
	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] < '0' || str[n] > '9')
		{
			ch = 0;
		}
		n++;
	}
	return (ch);
}
