#include <stdio.h>
#include <unistd.h>

int	ft_atoi(char *str)
{
	int	nb;
	int	i;
	int	x;

	nb = 0;
	i = 0;
	x = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	nb *= x;
	return (nb);
}
